ConfirmationLevel = str
ToolHandler = Callable[..., "ToolResult"]


@dataclass(slots=True)
class ToolSpec:
    """Inspectable tool metadata exposed to the runtime and CLI."""

    name: str
    description: str
    input_schema: dict[str, Any] = field(default_factory=dict)
    confirmation: ConfirmationLevel | None = None


@dataclass(slots=True)
class ToolCall:
    """Structured tool invocation generated by the agent or CLI."""

    name: str
    arguments: dict[str, Any] = field(default_factory=dict)


@dataclass(slots=True)
class ToolResult:
    """Result payload returned by one tool invocation."""

    name: str
    ok: bool
    content: str
    data: dict[str, Any] = field(default_factory=dict)
    error: str | None = None

    def to_message(self) -> str:
        status = "ok" if self.ok else "error"
        payload = {
            "status": status,
            "content": self.content,
            "data": self.data,
        }
        if self.error is not None:
            payload["error"] = self.error
        return json.dumps(payload, indent=2, sort_keys=True)


class ToolRegistry:
    """Simple in-memory registry of explicit workspace tools."""

    def __init__(self) -> None:
        self._specs: dict[str, ToolSpec] = {}
        self._handlers: dict[str, ToolHandler] = {}

    def register(self, spec: ToolSpec, handler: ToolHandler) -> None:
        self._specs[spec.name] = spec
        self._handlers[spec.name] = handler

    def specs(self) -> list[ToolSpec]:
        return [self._specs[name] for name in sorted(self._specs)]

    def get_spec(self, name: str) -> ToolSpec:
        return self._specs[name]

    def execute(self, tool_call: ToolCall) -> ToolResult:
        handler = self._handlers.get(tool_call.name)
        if handler is None:
            return ToolResult(name=tool_call.name, ok=False, content="", error=f"Unknown tool '{tool_call.name}'")
        try:
            return handler(**tool_call.arguments)
        except Exception as exc:  # noqa: BLE001
            return ToolResult(name=tool_call.name, ok=False, content="", error=str(exc))


class WorkspaceAppRunner:
    """Launch share/LeRobot scripts and persist their run metadata."""

    def __init__(self, store: WorkspaceStore, repo_root: Path | None = None):
        self.store = store
        self.repo_root = (repo_root or Path(__file__).resolve().parents[3]).resolve()

    def run_subprocess(
        self,
        *,
        project: str,
        task: str,
        run_type: str,
        command: list[str],
        prepared_run: RunRecord | None = None,
        mp_net: str | None = None,
        mp_net_version: str | None = None,
        primitive: str | None = None,
        policy_id: str | None = None,
        dataset_id: str | None = None,
        cwd: str | None = None,
        notes: str | None = None,
    ) -> RunRecord:
        """Run one subprocess, capture logs, and persist its run record."""
        record = prepared_run or self.store.create_run(
            project,
            task,
            run_type=run_type,
            status="running",
            command=command,
            cwd=cwd or str(self.repo_root),
            mp_net=mp_net,
            mp_net_version=mp_net_version,
            primitive=primitive,
            policy_id=policy_id,
            dataset_id=dataset_id,
            notes=notes,
        )
        record.run_type = run_type
        record.status = "running"
        record.command = command
        record.cwd = cwd or str(self.repo_root)
        record.mp_net = mp_net or record.mp_net
        record.mp_net_version = mp_net_version or record.mp_net_version
        record.primitive = primitive or record.primitive
        record.policy_id = policy_id or record.policy_id
        record.dataset_id = dataset_id or record.dataset_id
        record.notes = notes or record.notes
        self.store.save_run(project, task, record)
        run_dir = self.store.run_dir(project, task, record.run_id)
        completed = subprocess.run(
            command,
            cwd=cwd or self.repo_root,
            text=True,
            capture_output=True,
            check=False,
        )
        Path(record.stdout_path).write_text(completed.stdout, encoding="utf-8")
        Path(record.stderr_path).write_text(completed.stderr, encoding="utf-8")
        record.status = "succeeded" if completed.returncode == 0 else "failed"
        record.ended_at = utc_now()
        record.updated_at = record.ended_at
        summary_path = Path(record.summary_path) if record.summary_path is not None else run_dir / "summary.json"
        summary_payload = load_json(summary_path, default=None)
        if summary_payload is None:
            summary_payload = {
                "returncode": completed.returncode,
                "command": command,
                "stdout_path": record.stdout_path,
                "stderr_path": record.stderr_path,
            }
            dump_json(summary_path, summary_payload)
        record.summary_path = str(summary_path)
        if isinstance(summary_payload, dict):
            record.metrics = summary_payload.get("metrics", summary_payload)
            if "output_paths" in summary_payload and isinstance(summary_payload["output_paths"], list):
                record.output_paths = [str(item) for item in summary_payload["output_paths"]]
        self.store.save_run(project, task, record)
        return record


