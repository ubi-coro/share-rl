    def compare_runs(self, run_a: str, run_b: str) -> ToolResult:
        left = self.store.load_run(self.project, self.task, run_a)
        right = self.store.load_run(self.project, self.task, run_b)
        payload = {
            "run_a": asdict(left),
            "run_b": asdict(right),
            "metric_delta": self._metric_delta(left.metrics, right.metrics),
        }
        return ToolResult(name="compare_runs", ok=True, content=f"Compared runs '{run_a}' and '{run_b}'", data=payload)

    def _metric_delta(self, left: dict[str, Any], right: dict[str, Any]) -> dict[str, Any]:
        delta = {}
        for key in sorted(set(left) | set(right)):
            lhs = left.get(key)
            rhs = right.get(key)
            if isinstance(lhs, (int, float)) and isinstance(rhs, (int, float)):
                delta[key] = rhs - lhs
            else:
                delta[key] = {"left": lhs, "right": rhs}
        return delta

    def pin_artifact(
        self,
        kind: str,
        path: str,
        label: str | None = None,
        metadata: dict[str, Any] | None = None,
        mp_net: str | None = None,
    ) -> ToolResult:
        artifact = ArtifactRef(
            artifact_id=f"{kind}-{utc_now().replace(':', '-').replace('+00:00', 'z')}",
            kind=kind,
            path=str(Path(path).expanduser().resolve()),
            project=self.project,
            task=self.task,
            label=label,
            metadata=metadata or {},
            mp_net=mp_net,
        )
        self.store.save_artifact(self.project, self.task, artifact)
        return ToolResult(name="pin_artifact", ok=True, content=f"Pinned {kind} artifact", data={"artifact": asdict(artifact)})

