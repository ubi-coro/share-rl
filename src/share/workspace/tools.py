    def run_recording(
        self,
        dataset_repo_id: str,
        dataset_root: str,
        single_task: str,
        name: str | None = None,
        num_episodes: int = 1,
        episode_time_s: int = 30,
        interactive: bool = False,
    ) -> ToolResult:
        selected_name, version, config = self._load_selected_mpnet(name)
        run = self.store.create_run(
            self.project,
            self.task,
            run_type="record",
            status="preparing",
            mp_net=selected_name,
            mp_net_version=version,
            dataset_id=dataset_repo_id,
        )
        run_dir = self.store.run_dir(self.project, self.task, run.run_id)
        from share.configs.record import DatasetRecordConfig, RecordConfig

        record_cfg = RecordConfig(
            env=config,
            dataset=DatasetRecordConfig(
                repo_id=dataset_repo_id,
                root=dataset_root,
                single_task=single_task,
                num_episodes=num_episodes,
                episode_time_s=episode_time_s,
            ),
            interactive=interactive,
        )
        config_path = run_dir / "record_config.json"
        import draccus

        with open(config_path, "w", encoding="utf-8") as f, draccus.config_type("json"):
            draccus.dump(record_cfg, f, indent=2)
            f.write("\n")
        command = [sys.executable, "-m", "share.scripts.record", f"--config_path={config_path}"]
        record = self.runner.run_subprocess(
            project=self.project,
            task=self.task,
            run_type="record",
            command=command,
            prepared_run=run,
            mp_net=selected_name,
            mp_net_version=version,
            dataset_id=dataset_repo_id,
            cwd=str(self.repo_root),
        )
        return ToolResult(name="run_recording", ok=True, content="Launched recording workflow", data={"run": asdict(record)})

    def run_training(
        self,
        policy_path: str,
        dataset_repo_id: str,
        dataset_root: str,
        output_dir: str,
        steps: int = 1000,
        batch_size: int = 8,
        eval_freq: int = 0,
        job_name: str | None = None,
    ) -> ToolResult:
        prepared_run = self.store.create_run(
            self.project,
            self.task,
            run_type="training",
            status="preparing",
            policy_id=policy_path,
            dataset_id=dataset_repo_id,
        )
        summary_path = Path(prepared_run.summary_path)
        command = [
            sys.executable,
            "-m",
            "share.scripts.train",
            "--policy-path",
            policy_path,
            "--dataset-repo-id",
            dataset_repo_id,
            "--dataset-root",
            dataset_root,
            "--output-dir",
            output_dir,
            "--steps",
            str(steps),
            "--batch-size",
            str(batch_size),
            "--eval-freq",
            str(eval_freq),
            "--summary-path",
            str(summary_path),
        ]
        if job_name:
            command.extend(["--job-name", job_name])
        record = self.runner.run_subprocess(
            project=self.project,
            task=self.task,
            run_type="training",
            command=command,
            prepared_run=prepared_run,
            policy_id=policy_path,
            dataset_id=dataset_repo_id,
        )
        return ToolResult(name="run_training", ok=True, content="Launched training adapter", data={"run": asdict(record)})

    def run_evaluation(
        self,
        policy_path: str,
        dataset_root: str,
        output_dir: str,
        metrics_file: str | None = None,
    ) -> ToolResult:
        prepared_run = self.store.create_run(
            self.project,
            self.task,
            run_type="evaluation",
            status="preparing",
            policy_id=policy_path,
            dataset_id=dataset_root,
        )
        summary_path = Path(prepared_run.summary_path)
        command = [
            sys.executable,
            "-m",
            "share.scripts.eval_on_dataset",
            "--policy-path",
            policy_path,
            "--dataset-root",
            dataset_root,
            "--output-dir",
            output_dir,
            "--summary-path",
            str(summary_path),
        ]
        if metrics_file:
            command.extend(["--metrics-file", metrics_file])
        record = self.runner.run_subprocess(
            project=self.project,
            task=self.task,
            run_type="evaluation",
            command=command,
            prepared_run=prepared_run,
            policy_id=policy_path,
            dataset_id=dataset_root,
        )
        return ToolResult(name="run_evaluation", ok=True, content="Launched evaluation adapter", data={"run": asdict(record)})

    def register_run(
        self,
        run_type: str,
        status: str,
        notes: str | None = None,
        metrics: dict[str, Any] | None = None,
        command: list[str] | None = None,
    ) -> ToolResult:
        record = self.store.create_run(
            self.project,
            self.task,
            run_type=run_type,
            status=status,
            notes=notes,
            command=command or [],
        )
        record.metrics = metrics or {}
        record.ended_at = utc_now()
        self.store.save_run(self.project, self.task, record)
        return ToolResult(name="register_run", ok=True, content="Registered external run", data={"run": asdict(record)})
