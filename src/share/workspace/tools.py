    def edit_mpnet(
        self,
        operation: str,
        arguments: dict[str, Any],
        name: str | None = None,
        description: str | None = None,
    ) -> ToolResult:
        selected_name, _, config = self._load_selected_mpnet(name)
        updated = mpnet_utils.apply_edit(config, operation, arguments)
        payload = self._save_selected_mpnet(selected_name, updated, description=description, set_active=True)
        payload["summary"] = mpnet_utils.summarize_mpnet(updated)
        return ToolResult(
            name="edit_mpnet",
            ok=True,
            content=f"Applied '{operation}' to MP-Net '{selected_name}'",
            data=payload,
        )

