    def _load_selected_mpnet(self, name: str | None = None) -> tuple[str, str | None, Any]:
        task_record = self.store.ensure_task(self.project, self.task)
        selected_name = name or task_record.active_mp_net
        if selected_name is None:
            raise ValueError("No active MP-Net is set for the current task")
        path = self.store.mp_net_current_path(self.project, self.task, selected_name)
        if not path.exists():
            raise FileNotFoundError(f"MP-Net '{selected_name}' has no current.json in the workspace")
        config = mpnet_utils.load_mpnet_config(path)
        version = task_record.mp_nets[selected_name].current_version if selected_name in task_record.mp_nets else None
        return selected_name, version, config

    def _save_selected_mpnet(
        self,
        name: str,
        config: Any,
        *,
        description: str | None = None,
        origin_path: str | None = None,
        set_active: bool = True,
    ) -> dict[str, Any]:
        task_record = self.store.ensure_task(self.project, self.task)
        previous_version = task_record.mp_nets.get(name).current_version if name in task_record.mp_nets else None
        version_index = 1
        if previous_version and previous_version.startswith("v"):
            try:
                version_index = int(previous_version[1:]) + 1
            except ValueError:
                version_index = 1
        version_id = f"v{version_index:04d}"
        config = mpnet_utils.validate_mpnet_config(config)
        version_path = self.store.mp_net_version_path(self.project, self.task, name, version_id)
        current_path = self.store.mp_net_current_path(self.project, self.task, name)
        mpnet_utils.save_mpnet_config(config, version_path)
        shutil.copyfile(version_path, current_path)
        self.store.register_mp_net(
            self.project,
            self.task,
            name,
            version_id=version_id,
            origin_path=origin_path,
            description=description,
            set_active=set_active,
        )
        return {"name": name, "version_id": version_id, "path": str(current_path)}

    def status(self) -> ToolResult:
        workspace = self.store.ensure_workspace(project=self.project, task=self.task)
        task_record = self.store.ensure_task(self.project, self.task)
        runs = self.store.list_runs(self.project, self.task)[:5]
        artifacts = self.store.load_artifacts(self.project, self.task)[:5]
        payload = {
            "workspace": workspace.name,
            "project": self.project,
            "task": self.task,
            "active_mp_net": task_record.active_mp_net,
            "mp_nets": [asdict(item) for item in self.store.list_mp_nets(self.project, self.task)],
            "latest_run_id": task_record.latest_run_id,
            "recent_runs": [asdict(run) for run in runs],
            "recent_artifacts": [asdict(artifact) for artifact in artifacts],
        }
        return ToolResult(name="status", ok=True, content="Current task status", data=payload)

